n = int(input('Please enter an integer: '))
if n == 1:
    print('Monday')
elif n == 2:
    print('Tuesday')
elif n == 3:
    print('Wednesday')
elif n == 4:
    print('Thursday')
elif n == 5:
    print('Friday')
elif n == 6:
    print('Saturday')
elif n == 7:
    print('Sunday')
else:
    print('The provided integer does not correspond to a day!')